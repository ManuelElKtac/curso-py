import math


# round = va a devolver el numero al cual el numero se encuentre mas cercano, en este caso es 1
print(round(1.3))
print(round(1.5))

# abs = entragara el valor absoluto del numero que le pasemos a la funcion
print(abs(-77))
# el valor absoluto = saca el signo de la ecuacion, entonces siempre devuelve un numero positivo


# funciones de math:
print(math.ceil(1.1))
# ".ceil" = toma al numero y lo lleva al numero superior entero mas cercano
print(math.floor(1.9999))
# .floor = toma al numero y lo lleva al numero inferior entero mas cercano

# print(math.isnan("23")) = nos dira error
# .isnan = nos dira si el valor que le pasamos NO es un numero (solo si le pasamos numeros reales)
print(math.isnan(23))

print(math.pow(10, 3))  # nos permite elevar un numero a la potencia de algo

print(math.sqrt(9))  # saca la raiz cuadrada de un numero


